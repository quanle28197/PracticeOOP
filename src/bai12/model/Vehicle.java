package bai12.model;

public class Vehicle {
    protected int id;
    protected String manufacturer;
    protected int price;
    protected String color;

    public Vehicle(int id, String manufacturer, int price, String color) {
        this.id = id;
        this.manufacturer = manufacturer;
        this.price = price;
        this.color = color;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

}
